public class abbrevoperators{
	
	public static void main (String [] args){
		
		int result = 1; 
		// result = result + 1
		result++; 
		System.out.println(result);
		result--; 
		System.out.println(result);
		result += 2; 
		System.out.println(result);
		result *= 2; 
		System.out.println(result);
		result /= 2; 
		System.out.println(result);
		result -= 2; 
		System.out.println(result);
		
	}
	
}